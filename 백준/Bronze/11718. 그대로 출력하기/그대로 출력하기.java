
import java.io.*;
import java.util.*;

public class Main {

    public static void main(String[] args) throws Exception {
    	BufferedReader bf = new BufferedReader(new InputStreamReader(System.in));
    	
    	String result = "";
    	
    	while((result = bf.readLine()) != null) {
    			
    		System.out.println(result);
    		
    	}
    }
}